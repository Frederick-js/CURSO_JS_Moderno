
// Object literal
const producto = {
    nombre: "Monitor 20 pulgadas",
    precio: 300,
    disponible : true
    
}

// Object.keys  retorna la parte de la izquierda osea las llaves
console.log( Object.keys (producto));

// Object.values  retorna la parte de la derecha osea los valores
console.log( Object.values (producto));

// Object.entries  retorna todo en pares
console.log( Object.entries (producto));
